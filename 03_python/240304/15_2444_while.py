N = int(input()) # 5

#     *     # i = 1 , " " : 4, "*" : 1
#    ***    # i = 2 , " " : 3, "*" : 3
#   *****   # i = 3 , " " : 2, "*" : 5
#  *******  # i = 4 , " " : 1, "*" : 7
# ********* # i = 5 , " " : 0, "*" : 9

i = 1
while i <= N :
    space = N - i
    star = 2 * i - 1
    print(" " * space + "*" * star)
    i += 1

#  *******  # i=1 ," " : 1, "*" : 7    4
#   *****   # i=2 ," " : 2, "*" : 5    3
#    ***    # i=3 ," " : 3, "*" : 3    2
#     *     # i=4 ," " : 4, "*" : 1    1

i = 1
while i < N:
    space = i
    star = 2 * (N - i) - 1
    print(" " * space + "*" * star)
    i += 1

